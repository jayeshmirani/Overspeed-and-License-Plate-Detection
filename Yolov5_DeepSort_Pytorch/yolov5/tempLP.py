def addNewEntry(id, startFrame, endFrame, speed, license_plate):
    
    id = int(id)
    startFrame = int(startFrame)
    endFrame = int(endFrame)
    speed = int(speed)
    next_entry_index = int(str(sys.argv[6]))
    # license_plate = int(license_plate)

